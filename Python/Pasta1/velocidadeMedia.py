#FUP que le o espaço percorrido e o tempo. E entao calcule a Velocidade Media

print("Velocidade Média")

Espaco=float(input('Digite a distancia percorrida em KM: '))
Tempo=float(input("Digite o tempo percorrido em horas: "))
VelocidadeMedia= Espaco / Tempo

print(f'A Velocidade Média é: {VelocidadeMedia}')
